void BlockClass::Render(ID3D11Device* device, ID3D11DeviceContext* context)
{
	HRESULT hr;
	Propertys Combine;
	D3DXMatrixTranslation(&propertys.TranslationMatrix, m_position[0], m_position[1], m_position[2]);
	D3DXMatrixRotationYawPitchRoll(&propertys.RotationMatrix, m_rotation[0], m_rotation[1], m_rotation[2]);
	D3DXMatrixScaling(&propertys.ScalingMatrix, m_scaling[0], m_scaling[1], m_scaling[2]);
	D3DXMatrixTranspose(&Combine.TranslationMatrix, &propertys.TranslationMatrix);
	D3DXMatrixTranspose(&Combine.ScalingMatrix, &propertys.ScalingMatrix);
	D3DXMatrixTranspose(&Combine.RotationMatrix, &propertys.RotationMatrix);

	m_PRS.UpdateData(&Combine);
	m_PRS.UpdateGpu(device, context);
	m_PRS.BindVertexShader(device, context);
	context->IASetPrimitiveTopology(D3D11_PRIMITIVE_TOPOLOGY_TRIANGLELIST);
	m_vertBuffer.Render(device, context);
}

